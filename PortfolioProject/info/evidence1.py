# Kitchen, Bedroom, Basement
class Evidence:
    def Kitchen(self, kitchen):
        self.kitchen = kitchen
        print("the evidence in the kitchen is", self.kitchen)

    def Bedroom(self, bed):
        self.bed = bed
        print("the evidence in the bedroom is", self.bed)

    def Basement(self, basement):
        self.basement = basement
        print("the evidence in the bedroom is", self.basement)
